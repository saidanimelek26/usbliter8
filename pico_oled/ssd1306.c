void ssd1306_draw_text(int x, int y, const char *text) {
    if (!text) return;
    if (x >= LCD_WIDTH_LOGICAL) return;
    if (y % 8 != 0) return;
    
    int page = y / 8;
    if (page >= LCD_PAGES) return;
    
    int col = x;
    
    for (const char *p = text; *p && col < LCD_WIDTH_LOGICAL - 6; p++) {
        unsigned char c = (unsigned char)*p;
        
        // Handle ASCII printable characters (32-126)
        if (c >= 32 && c <= 126) {
            const uint8_t *glyph = font_5x7[c - 32];
            
            for (int i = 0; i < 5; ++i) {
                if (col >= 0 && col < LCD_WIDTH_LOGICAL) {
                    buffer[page * LCD_WIDTH_LOGICAL + col] = glyph[i];
                }
                col++;
            }
            
            // Add space between characters
            if (col < LCD_WIDTH_LOGICAL) {
                buffer[page * LCD_WIDTH_LOGICAL + col] = 0x00;
                col++;
            }
        }
    }
}
