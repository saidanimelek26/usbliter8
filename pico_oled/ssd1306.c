// SH1106 OLED Driver - 128x64 with internal 132-column controller
// This driver supports SH1106 displays with proper page addressing
// and column offset handling for 128x64 viewable area

#define LCD_WIDTH_LOGICAL 128
#define LCD_HEIGHT 64
#define LCD_PAGES (LCD_HEIGHT/8)
#define SH1106_COL_OFFSET 2

static i2c_inst_t *i2c_port = NULL;
static uint8_t i2c_addr = 0x3C;
static uint8_t buffer[LCD_WIDTH_LOGICAL * LCD_PAGES];

// SH1106 Initialization Sequence - Optimized for 128x64
static const uint8_t init_sequence[] = {
    0xAE,           // Display OFF
    0xD5, 0x80,     // Set oscillator frequency
    0xA8, 0x3F,     // Set multiplex ratio (64)
    0xD3, 0x00,     // Set display offset
    0x40,           // Set display start line
    0x8D, 0x14,     // Enable charge pump
    0xA1,           // Set segment remap
    0xC8,           // Set COM output scan direction
    0xDA, 0x12,     // Set COM pins configuration
    0x81, 0xCF,     // Set contrast (maximum recommended for SH1106)
    0xD9, 0xF1,     // Set pre-charge period
    0xDB, 0x40,     // Set V_COMH deselect level
    0xA4,           // Entire display ON (from RAM)
    0xA6,           // Normal display mode
    0xAF            // Display ON
};

static int write_cmd(const uint8_t *cmds, size_t len) {
    if (!i2c_port) return -1;
    uint8_t tx[len + 1];
    tx[0] = 0x00;  // Control byte for commands
    memcpy(&tx[1], cmds, len);
    return i2c_write_blocking(i2c_port, i2c_addr, tx, (int)(len + 1), false);
}

static int write_data(const uint8_t *data, size_t len) {
    if (!i2c_port) return -1;
    const size_t chunk = 16;
    uint8_t tx[chunk + 1];
    tx[0] = 0x40;  // Control byte for data
    size_t sent = 0;
    
    while (sent < len) {
        size_t tosend = (len - sent) > chunk ? chunk : (len - sent);
        memcpy(&tx[1], &data[sent], tosend);
        int rc = i2c_write_blocking(i2c_port, i2c_addr, tx, (int)(tosend + 1), false);
        if (rc < 0) return rc;
        sent += tosend;
    }
    return 0;
}

bool ssd1306_init_i2c(i2c_inst_t *i2c, uint8_t address) {
    if (!i2c) return false;
    
    i2c_port = i2c;
    i2c_addr = address;
    
    sleep_ms(50);
    
    if (write_cmd(init_sequence, sizeof(init_sequence)) < 0) {
        return false;
    }
    
    ssd1306_clear();
    ssd1306_update();
    sleep_ms(100);
    
    return true;
}

void ssd1306_clear(void) {
    memset(buffer, 0, sizeof(buffer));
}

void ssd1306_update(void) {
    // SH1106 requires page-by-page updates with manual column addressing
    for (int page = 0; page < LCD_PAGES; ++page) {
        // Set page address (0xB0-0xB7)
        uint8_t page_cmd = 0xB0 | (page & 0x07);
        if (write_cmd(&page_cmd, 1) < 0) return;
        
        // Set column address with SH1106 offset
        // Column address is set using lower nibble (0x00-0x0F) and higher nibble (0x10-0x1F)
        uint8_t col = SH1106_COL_OFFSET;
        uint8_t col_low = 0x00 | (col & 0x0F);
        uint8_t col_high = 0x10 | ((col >> 4) & 0x0F);
        uint8_t col_cmds[2] = { col_low, col_high };
        if (write_cmd(col_cmds, 2) < 0) return;
        
        // Write page data
        const uint8_t *page_data = &buffer[page * LCD_WIDTH_LOGICAL];
        if (write_data(page_data, LCD_WIDTH_LOGICAL) < 0) return;
    }
}

